package gameoflife;

/**
 * @author sameer
 *
 */
public class Cell {
	private int x;
	
	private int y;

	/**
	 * @param x
	 * @param y
	 */
	public Cell(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
	

}
